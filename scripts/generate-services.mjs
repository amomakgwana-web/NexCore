#!/usr/bin/env node
// Generates services/<name>/{index.js,package.json,Dockerfile} for every
// service in services/manifest.json. Hero services (auth, payroll, billing,
// accounting, finance) get a scaffold too, but their index.js is hand-written
// with real business logic afterwards — this script will NOT overwrite an
// index.js that already exists, so it's safe to re-run after hand-editing.
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const manifest = JSON.parse(fs.readFileSync(path.join(root, 'services/manifest.json'), 'utf8'));

const PKG_TEMPLATE = (name) => JSON.stringify({
  name,
  version: '1.0.0',
  private: true,
  main: 'index.js',
  scripts: { start: 'node index.js' },
  dependencies: {
    express: '^4.21.2',
    '@supabase/supabase-js': '^2.45.4',
    cors: '^2.8.5',
    compression: '^1.7.4',
    morgan: '^1.10.0',
  },
}, null, 2) + '\n';

const DOCKERFILE_TEMPLATE = () => `FROM node:20-alpine
WORKDIR /app
COPY package.json ./
RUN npm install --omit=dev
COPY index.js ./
COPY ../_shared ./_shared
ENV PORT=4000
EXPOSE 4000
CMD ["node", "index.js"]
`;

function crudIndexJs(svc) {
  const resourceLines = (svc.resources || []).map(r =>
    `mountCrud(app, { table: ${JSON.stringify(r.table)}, resource: ${JSON.stringify(r.resource)}, idColumn: ${JSON.stringify(r.idColumn || 'id')}, supabase, defaultOrder: null });`
  ).join('\n');

  return `// Auto-generated by scripts/generate-services.mjs from services/manifest.json.
// Real CRUD service — every route performs an actual Supabase call, no mocks.
// Re-running the generator will NOT touch this file if you convert it to a
// hero service with bespoke logic; delete it first if you want it regenerated.
const path = require('path');
const { createService, mountCrud, getSupabaseClient, start } = require(path.join(__dirname, '../_shared/serviceFactory'));

const { app } = createService(${JSON.stringify(svc.name)});
const supabase = getSupabaseClient();

${resourceLines || '// (no CRUD resources for this service)'}

start(app, ${JSON.stringify(svc.name)});
`;
}

for (const svc of manifest.services) {
  const dir = path.join(root, 'services', svc.name);
  fs.mkdirSync(dir, { recursive: true });

  fs.writeFileSync(path.join(dir, 'package.json'), PKG_TEMPLATE(svc.name));
  fs.writeFileSync(path.join(dir, 'Dockerfile'), DOCKERFILE_TEMPLATE());

  const indexPath = path.join(dir, 'index.js');
  if (!fs.existsSync(indexPath)) {
    fs.writeFileSync(indexPath, crudIndexJs(svc));
    console.log(`generated ${svc.name}/index.js (CRUD)`);
  } else {
    console.log(`skipped ${svc.name}/index.js (already exists)`);
  }
}
console.log(`\nDone. ${manifest.services.length} services scaffolded under services/.`);
